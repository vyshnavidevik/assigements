   SELECT	JOB,
			DEPTNO, 
			AVG(SAL*12) AS 'ANNUAL_SAL'
   FROM EMP
   GROUP BY JOB ,DEPTNO;
   
   
   SELECT	COUNT(SAL) AS 'TOTAL_SAL',
			COUNT(COMM) AS 'TOTAL_COMM'
   FROM EMP
   WHERE DEPTNO = 30 ;

   
   SELECT	AVG(SAL) AS 'AVERAGE', 
			MIN(SAL) AS 'MINIMUM',
			MAX(SAL) AS 'MAXIMUM'
   FROM EMP
   WHERE JOB = 'CLERK' OR JOB ='MANAGER' ;

   
   SELECT DEPTNO
   FROM EMP
   WHERE EXISTS (SELECT COUNT(JOB) AS CLERK_COUNT 
                FROM EMP
                WHERE JOB = 'CLERK'
                GROUP BY DEPTNO)
   GROUP BY DEPTNO;

   
